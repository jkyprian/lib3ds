/*!
* \ingroup chunk
*/
const char*
lib3ds_chunk_name(Lib3dsWord chunk)
{
    Lib3dsChunkTable *p;

    for (p=lib3ds_chunk_table; p->name!=0; ++p) {
        if (p->chunk==chunk) {
            return(p->name);
        }
    }
    return("***UNKNOWN***");
}
